import cv2
import numpy as np

image=cv2.imread('image.png')
robertsx=np.array([[1, 0], [0, -1]])
robertsy=np.array([[0, -1], [1, 0]])
prewittx=np.array([[-1, 0, 1], [-1, 0, 1], [-1, 0, 1]])
prewitty=np.array([[-1, -1, -1], [0, 0, 0], [1, 1, 1]])
sobelx=np.array([[-1, 0, 1], [-2, 0, 2], [-1, 0, 1]])
sobely=np.array([[-1, -2, -1], [0, 0, 0], [1, 2, 1]])
laplacian=np.array([[0, -1, 0], [-1, 4, -1], [0, -1, 0]])
sharpen=np.array([[0, -1, 0], [-1, 5, -1], [0, -1, 0]])
image1=np.abs(cv2.filter2D(image, -1, prewittx)+cv2.filter2D(image, -1, prewitty))
image2=np.abs(cv2.filter2D(image, -1, sobelx)+cv2.filter2D(image, -1, sobely))
image3=np.abs(cv2.filter2D(image, -1, robertsx)+cv2.filter2D(image, -1, robertsy))
image4=cv2.Canny(image, 50, 80)
image5=cv2.filter2D(image, -1, sharpen)
image6=cv2.filter2D(image, -1, laplacian)
cv2.imwrite('prewitt.png', image1)
cv2.imwrite('sobel.png', image2)
cv2.imwrite('roberts.png', image3)
cv2.imwrite('canny.png', image4)
cv2.imwrite('sharpen.png', image5)
cv2.imwrite('laplacian.png', image6)